import { generateClient } from "./generateClient.js";
import { generateRegistry } from "./generateRegistry.js";
import { generateRpcs } from "./generateRpcs.js";
import { generateSchemas } from "./generateSchemas.js";
import { generateServer } from "./generateServer.js";
import { effectImportPath, pbImportPath } from "./naming.js";
import type { GeneratorFile } from "./types.js";

export const generateFile = (file: GeneratorFile): string => {
  const pbImport = pbImportPath(file.protoFileName, file.importExtension);
  const descriptorImports = file.services.map((service) => service.name);
  const hasServices = file.services.length > 0;
  const effectImports = [
    ...(hasServices ? ["Context", "Effect", "Layer"] : []),
    "Schema",
    ...(hasServices ? ["Stream"] : []),
  ];

  const lines = [
    "// @generated by protoc-gen-effect-grpc. DO NOT EDIT.",
    "",
    `import { ${effectImports.join(", ")} } from "effect";`,
    ...(hasServices
      ? [
          'import { Rpc, RpcClient, RpcClientError, RpcGroup } from "effect/unstable/rpc";',
          'import { CodegenSupport, GrpcMethodRegistry, GrpcStatusError } from "@effect-grpc/effect-grpc";',
          "import {",
          ...descriptorImports.map((item) => `  ${item},`),
          `} from "${pbImport}";`,
        ]
      : []),
    ...file.imports.flatMap((imported) => [
      "import {",
      ...imported.enums.flatMap((enumName) => [
        `  ${enumName}Schema,`,
        `  type ${enumName},`,
      ]),
      ...imported.messages.flatMap((message) => [
        `  ${message}Schema,`,
        `  from${message},`,
        `  to${message},`,
        `  type ${message},`,
      ]),
      `} from "${effectImportPath(file.protoFileName, imported.protoFileName, file.importExtension)}";`,
    ]),
    "",
    ...generateSchemas(file),
    ...generateRpcs(file),
    ...generateRegistry(file),
    ...generateClient(file),
    ...generateServer(file),
  ];

  return `${lines.join("\n").trimEnd()}\n`;
};
